    private void deleteButtomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtomActionPerformed
         
         int linha = listaContatos.getSelectedRow();

    if (linha == -1) {
        JOptionPane.showMessageDialog(this, "selecione um contato");
        return;
    }

    int opcao = JOptionPane.showConfirmDialog(
        this,
        "tem certeza?",
        "confirmação",
        JOptionPane.YES_NO_OPTION
    );

    if (opcao == JOptionPane.YES_OPTION) {
        agenda.remover(linha);
        carregarTabela(agenda);
    }
    }//GEN-LAST:event_deleteButtomActionPerformed

    private void searchButtomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtomActionPerformed
        String textoBusca = busca.getText();
       
        
        if(textoBusca.isEmpty()){
             listaContatos.setRowSorter(null);
             JOptionPane.showMessageDialog(this,"preencha o campo de busca");
             return;
        } else{
            
        DefaultTableModel modelo = (DefaultTableModel) listaContatos.getModel();
        TableRowSorter<DefaultTableModel> sort = new TableRowSorter<>(modelo);
        
        
        listaContatos.setRowSorter(sort);
            sort.setRowFilter(RowFilter.regexFilter("(?i)"+ textoBusca, 0,1,2,3,9) );
        }
     
    }//GEN-LAST:event_searchButtomActionPerformed

