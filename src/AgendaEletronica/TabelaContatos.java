    public void carregarTabela(Agenda agenda){
    DefaultTableModel modelo = (DefaultTableModel) listaContatos.getModel();
    modelo.setRowCount(0);//LIMPA a tabela
    
       System.out.println("Quantidade de contatos: " + agenda.getContatos().size());
    
    for (Contato contato : agenda.getContatos()){
        
        modelo.addRow(new Object []{
        
            contato.getCpf(),
            contato.getNome(),
            contato.getEmail(),
            contato.getTelefone(),
        
            });
        }
    }
  
