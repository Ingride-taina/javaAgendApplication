    private void saveButtom1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtom1ActionPerformed
      String nome= campoNome.getText();
      String cpf= campoCpf.getText();
      String email= campoEmail.getText();
      String telefone= campoTelefone.getText();
      
      if (nome.isEmpty()||
          cpf.isEmpty()||
          email.isEmpty()||
          telefone.isEmpty()){
            JOptionPane.showMessageDialog(this,"Todos os campos precisam ser preenchidos");
            return;
      }
      
      Contato conato1= new Contato(cpf, nome, email, telefone);
      agenda.adicionar(conato1);
      
      JOptionPane.showMessageDialog(this,"Contato adicionado!");
      limparCampos();
    }   
    
     private void  limparCampos(){
     for (java.awt.Component contato : getContentPane().getComponents()){
         if (contato instanceof javax.swing.JTextField){
             /* esta regra abaixo serve para o java tratar o Component "contato"
             como um JTextField, para n√£o ocorrer erros, pois, 
             nem todo Component tem setText() e nesta regra passamos contato como 
             JTextField com o cast
             */
                ((javax.swing.JTextField) contato).setText("");
         
         }
     
        }
     
     }
    

